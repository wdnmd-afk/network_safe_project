function createScenario(input) {
  const signalPrefix = input.id.replaceAll(".", "-");

  return {
    id: input.id,
    slug: input.scene,
    category: input.category,
    subcategory: input.scene,
    title: input.title,
    mode: input.mode,
    severity: input.severity,
    difficulty: input.difficulty,
    summary: input.summary,
    phase: phaseByCategory[input.category],
    tags: input.tags,
    knowledgePoints: input.knowledgePoints,
    defaultScenarioKey: input.scenarioKey,
    defaultControlKey: input.weakControlKey,
    scenarios: [
      {
        key: input.scenarioKey,
        title: input.scenarioTitle,
        description: input.scenarioDescription,
        riskIndicators: input.riskIndicators,
        rootCause: input.rootCause,
      },
    ],
    controls: [
      {
        key: input.weakControlKey,
        title: input.weakControlTitle,
        description: `观察未落实“${input.defense}”时的风险判定。`,
        fixedDecision: "blocked",
        fixedSignal: `${signalPrefix}-defense-blocked`,
        fixedMessage: `修复版识别到高风险固定案例，并通过${input.defense}阻断该动作。`,
      },
      {
        key: input.strongControlKey,
        title: input.strongControlTitle,
        description: `验证落实“${input.defense}”后的正常受控流程。`,
        fixedDecision: "accepted",
        fixedSignal: `${signalPrefix}-normal-verified`,
        fixedMessage: `修复版确认${input.defense}已落实，固定正常流程可以继续。`,
      },
    ],
    vulnerableOutcome: {
      decision: "accepted",
      signal: `${signalPrefix}-risk-accepted`,
      message: `漏洞版因${input.rootCause}接受了固定高风险动作。`,
    },
    safeBoundaries: [
      categoryBoundary[input.category],
      "页面和 API 只接受共享目录中声明的 scenarioKey 与 controlKey。",
      "未知 key 会被脱敏阻断，不写入原始输入或外部目标信息。",
      ...(input.safeBoundaries ?? []),
    ],
    notes:
      input.mode === "case-study"
        ? "该实验按 case-study ready 例外收口，不提供 exploit.py、攻击脚本、外部连接或真实样本。"
        : "该实验仅提供本机受控固定场景和只读验证，不允许扩展为通用攻击工具。",
  };
}


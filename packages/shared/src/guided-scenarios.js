export const guidedScenarioCatalog = Object.freeze(
  scenarioInputs.map((input) => Object.freeze(createScenario(input))),
);

export function getGuidedScenarioById(id) {
  return guidedScenarioCatalog.find((scenario) => scenario.id === id);
}

export function getGuidedScenarioByRoute(category, scene) {
  return guidedScenarioCatalog.find(
    (scenario) =>
      scenario.category === category && scenario.subcategory === scene,
  );
}

export function listGuidedScenarioIds() {
  return guidedScenarioCatalog.map((scenario) => scenario.id);
}
